    public static int chalkReplacer(int[] chalk, int k) {
        long sum = 0;
        for (int i : chalk) sum += i;
        k %= sum;
        int count = 0;
        while (k > 0) k -= chalk[count++];
        return k == 0 ? count : count - 1;
    }

